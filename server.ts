// POST endpoint to handle natural language Gemini commands
app.post("/api/gemini/command", async (req, res) => {
  const { query, currentWidgets } = req.body;

  if (!query) {
    return res.status(400).json({ error: "Missing query string." });
  }

  const ai = getGeminiClient();
  if (!ai) {
    console.log("Gemini API Client offline. Emulating natural language command:", query);
    const emulated = emulateQueryResponse(query, currentWidgets, false);
    return res.json(emulated);
  }

  try {
    const systemPrompt = `
      You are the backend AI layout and analytics engine for "Gridify", a highly polished drag-and-drop telemetry dashboard.
      The user provides a natural language query: "${query}".
      
      Here is the current list of dashboard widgets:
      ${JSON.stringify(currentWidgets, null, 2)}
      
      Here is the raw telemetry data:
      ${JSON.stringify(defaultTelemetry, null, 2)}
      
      Your job is to respond in JSON format, containing:
      1. 'aiSummary': An array of 3-5 strings presenting concise, polished analytical bullet points or insights about the telemetry data relative to the query. Keep bullet points elegant, brief, and highly realistic.
      2. 'feedbackMessage': A clean, professional 1-sentence confirmation of what action you performed (e.g. "I've added a custom Sensor Voltage area chart to your dashboard.").
      3. 'newWidgets': (Optional) If the user asked to ADD a chart, CREATE a view, REMOVE a chart, or TOGGLE things, return the updated or newly designed array of widgets.
         - To ADD a widget: Create a new object with an ID like "custom_X", type: "custom_chart", title: a suitable title, subtitle: descriptive, w: 2 or 3, and a 'customData' object with 'labels' (array of strings), 'values' (array of numbers), and 'yAxisLabel' (string).
         - To REMOVE: filter out matching items from the currentWidgets list.
         - Otherwise, return currentWidgets or omit if no visual change is required.

      Keep the tone helpful, professional, and objective.
    `;

    const response = await ai.models.generateContent({
      model: "gemini-3.5-flash",
      contents: systemPrompt,
      config: {
        responseMimeType: "application/json",
        responseSchema: {
          type: Type.OBJECT,
          properties: {
            aiSummary: {
              type: Type.ARRAY,
              items: { type: Type.STRING },
              description: "Array of analytical insight statements or summaries."
            },
            feedbackMessage: {
              type: Type.STRING,
              description: "Brief user-facing confirmation of AI action taken."
            },
            newWidgets: {
              type: Type.ARRAY,
              description: "Updated widgets list if adding/removing/editing layout, otherwise leave empty or match original.",
              items: {
                type: Type.OBJECT,
                properties: {
                  id: { type: Type.STRING },
                  title: { type: Type.STRING },
                  subtitle: { type: Type.STRING },
                  type: { type: Type.STRING },
                  w: { type: Type.INTEGER },
                  h: { type: Type.INTEGER },
                  order: { type: Type.INTEGER },
                  customData: {
                    type: Type.OBJECT,
                    properties: {
                      labels: { type: Type.ARRAY, items: { type: Type.STRING } },
                      values: { type: Type.ARRAY, items: { type: Type.NUMBER } },
                      yAxisLabel: { type: Type.STRING }
                    }
                  }
                }
              }
            }
          },
          required: ["aiSummary", "feedbackMessage"]
        }
      }
    });

    const parsed = JSON.parse(response.text || "{}");
    res.json({
      aiSummary: parsed.aiSummary,
      feedbackMessage: parsed.feedbackMessage,
      newWidgets: parsed.newWidgets || currentWidgets,
      status: "Nominal"
    });

  } catch (error: any) {
    console.error("Gemini model execution error (falling back to local emulator):", error);
    // Graceful fallback during 503 / high-demand scenarios
    const fallbackResponse = emulateQueryResponse(query, currentWidgets, true);
    res.json(fallbackResponse);
  }
});

// Configure Vite middleware or production static folder
async function setupServer() {
  if (process.env.NODE_ENV !== "production") {
    console.log("Starting server in development mode with Vite middleware...");
    const vite = await createViteServer({
      server: { middlewareMode: true },
      appType: "spa",
    });
    app.use(vite.middlewares);
  } else {
    console.log("Starting server in production mode with static hosting...");
    const distPath = path.join(process.cwd(), "dist");
    app.use(express.static(distPath));
    app.get("*", (req, res) => {
      res.sendFile(path.join(distPath, "index.html"));
    });
  }

  app.listen(PORT, "0.0.0.0", () => {
    console.log(`Gridify server listening on http://localhost:${PORT}`);
  });
}

setupServer();
