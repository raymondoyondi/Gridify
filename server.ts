// Helper to emulate natural language telemetry commands cleanly in offline or fallback modes
function emulateQueryResponse(query: string, currentWidgets: any[], isFallbackMode: boolean = false) {
  let feedbackMessage = isFallbackMode
    ? "Gridify layout engine running in backup mode due to temporary upstream model load."
    : "Configured layout and analyzed metrics successfully (Offline mode).";
  
  let newWidgets = [...(currentWidgets || [])];
  let aiSummary = [
    "Temperature trends show normal cyclical variance. Check peak loads during afternoon operations.",
    "Proxy devices maintain stable active flow control states with minor load offsets.",
    "Marchival Arc is exhibiting slightly higher average node temperature latency; investigate status indicator.",
  ];

  const lowerQuery = query.toLowerCase();
  if (lowerQuery.includes("add") || lowerQuery.includes("chart") || lowerQuery.includes("create")) {
    const id = "custom_" + Date.now();
    newWidgets.push({
      id,
      title: "Dynamic Telemetry: Sensor Voltage",
      subtitle: isFallbackMode ? "Generated by backup layout engine" : "Generated by Gemini AI layout engine",
      type: "custom_chart",
      w: 2,
      h: 300,
      order: newWidgets.length + 1,
      customData: {
        labels: ["00:00", "04:00", "08:00", "12:00", "16:00", "20:00"],
        values: [3.3, 3.2, 3.4, 3.1, 3.5, 3.3],
        yAxisLabel: "Volts (V)"
      }
    });
    feedbackMessage = isFallbackMode
      ? "Added dynamic Sensor Voltage chart (emulated fallback due to temporary model demand)."
      : "Added a dynamic Sensor Voltage chart to the layout!";
    aiSummary.unshift("Dynamic telemetry configured: Voltage levels average 3.3V, showing healthy battery load.");
  } else if (lowerQuery.includes("remove") || lowerQuery.includes("delete")) {
    if (newWidgets.length > 2) {
      const removed = newWidgets.pop();
      feedbackMessage = `Removed widget: "${removed.title}" from the active workspace.`;
    } else {
      feedbackMessage = "Kept primary widgets to maintain minimum dashboard visualization integrity.";
    }
  } else if (lowerQuery.includes("summarize") || lowerQuery.includes("analyse") || lowerQuery.includes("analyze")) {
    feedbackMessage = isFallbackMode
      ? "Completed telemetry load and uptime analysis (backup mode)."
      : "Completed deeper telemetry load and uptime analysis.";
    aiSummary = [
      "Device load: Home Hub averages 2.3K loads with optimal 26.78 Sensor load values.",
      "System uptime is steady at 3h 10m aggregate uptime with zero hard reboots.",
      "No severe performance drops are registered for any of the 10 monitored operational nodes.",
    ];
  } else {
    feedbackMessage = isFallbackMode
      ? `Processed custom query: "${query}" (backup emulation)`
      : `Processed custom query: "${query}"`;
  }

  return {
    aiSummary,
    feedbackMessage,
    newWidgets,
    status: isFallbackMode ? "Nominal (Backup Mode)" : "Nominal"
  };
}

